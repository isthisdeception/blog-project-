"""
URL configuration for blog_project project.

Routes are organized by app:
- blog/ -> Blog app (posts, categories, tags)
- accounts/ -> Accounts app (auth, profile)
- pages/ -> Pages app (about, contact, newsletter)
"""

from django.contrib import admin
from django.urls import path, include
from django.conf import settings
from django.conf.urls.static import static
from django.contrib.auth import views as auth_views

urlpatterns = [
    path('admin/', admin.site.urls),
    
    # Blog app URLs (main content)
    path('', include('blog.urls', namespace='blog')),
    
    # Authentication and profile URLs
    path('accounts/', include('accounts.urls', namespace='accounts')),
    
    # Static pages (about, contact, etc.)
    path('pages/', include('pages.urls', namespace='pages')),
    
    # Password reset URLs (Django's built-in views)
    path('password-reset/', 
         auth_views.PasswordResetView.as_view(
             template_name='accounts/password_reset.html',
             email_template_name='accounts/password_reset_email.html',
             subject_template_name='accounts/password_reset_subject.txt'
         ), 
         name='password_reset'),
    path('password-reset/done/', 
         auth_views.PasswordResetDoneView.as_view(
             template_name='accounts/password_reset_done.html'
         ), 
         name='password_reset_done'),
    path('password-reset-confirm/<uidb64>/<token>/', 
         auth_views.PasswordResetConfirmView.as_view(
             template_name='accounts/password_reset_confirm.html'
         ), 
         name='password_reset_confirm'),
    path('password-reset-complete/', 
         auth_views.PasswordResetCompleteView.as_view(
             template_name='accounts/password_reset_complete.html'
         ), 
         name='password_reset_complete'),
]

# Serve media files in development
if settings.DEBUG:
    urlpatterns += static(settings.MEDIA_URL, document_root=settings.MEDIA_ROOT)
    urlpatterns += static(settings.STATIC_URL, document_root=settings.STATIC_ROOT)

# Custom error handlers
handler404 = 'pages.views.custom_404_view'
